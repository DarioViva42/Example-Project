package com.example.application.model;

public class MemoryConfiguration {
	private int numberOfModules;
	private int memoryPerModule;

	public int getNumberOfModules() {
		return numberOfModules;
	}

	public void setNumberOfModules(int numberOfModules) {
		this.numberOfModules = numberOfModules;
	}

	public int getMemoryPerModule() {
		return memoryPerModule;
	}

	public void setMemoryPerModule(int memoryPerModule) {
		this.memoryPerModule = memoryPerModule;
	}
}
